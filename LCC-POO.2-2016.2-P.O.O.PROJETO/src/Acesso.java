
public class Acesso {
	private String log;
	private String sen;
	private String cog;

	public Acesso(String log, String sen, String cog) {
		super();
		this.log = log;
		this.sen = sen;
		this.cog = cog;
	}

	public String getLog() {
		return log;
	}

	public void setLog(String log) {
		this.log = log;
	}

	public String getSen() {
		return sen;
	}

	public void setSen(String sen) {
		this.sen = sen;
	}

	public String getCog() {
		return cog;
	}

	public void setCog(String cog) {
		this.cog = cog;
	}

}
