package Questao04;

public class Valor {
	private double valor;

	public Valor(double valor) {
		this.valor = valor;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(double valor) {
		this.valor = valor;
	}

	public String toString() {
		return "" + this.valor;
	}

}
