package Questao02;

public class Codigo {
	private String codigo;

	public Codigo(String codigo) {
		this.codigo = codigo;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String toString() {
		return this.codigo;
	}

}
